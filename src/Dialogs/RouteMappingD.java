    public void Guardar() {
        if (!compCacillas()) {
            String nreg = lblID.getText() + ","
                    + cmbCobradores.getSelectedItem().toString() + ","
                    + new SimpleDateFormat("dd-MM-YYYY").format(new Date()) + ","
                    + new SimpleDateFormat("HH:MM").format(new Date()) + ",";
            if (btnAceptar.getText().equals("GUARDAR")) {
                try {
                    int DB = JOptionPane.showConfirmDialog(null, "<html><h1>AÃADIR NUEVO MAPEO DE RUTAS</h1><font SIZE=5><p> Desea crear un nuevo Registro?</font></html>", "NUEVO REGISTRO", JOptionPane.YES_NO_OPTION);
                    if (DB == 0) {
                        new RouteMapping().insRouteMapping(nreg);
                        saveListRoutes();
                        JOptionPane.showMessageDialog(null, "<html><h1>REGISTRO GUARDADO CORRECTAMENTE</h1><font SIZE=5><p> Clic para cerrar...</font></html>", "REGISTRO GUARDADO", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e);
                }
            } else {
                try {
                    int dialogButton = JOptionPane.showConfirmDialog(null, "Desea actualizar este Cliente?", "ACTUALIZAR CLIENTE", JOptionPane.YES_NO_OPTION);
                    if (dialogButton == 0) {
//                        new RouteMapping().updateCollector(nreg);
                        JOptionPane.showMessageDialog(this, "Cliente actualizado Correctamente");
                        this.dispose();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error: " + e);
                }
            }
        }
    }

    public void saveListRoutes() {
        List<String> save = new ArrayList<>();
        for (int i = 0; i < Rutas.size(); i++) {
            String nreg = new RouteMapping().genIDB() + ","
                    + Rutas.get(i) + ","
                    + lblID.getText() + ",";
            new RouteMapping().insRouteMappingB(nreg);
        }
    }

    public boolean compCacillas() {
        boolean resp = false;
        if ("SIN COBRADORES".equals(cmbCobradores.getSelectedItem().toString())) {
            cmbCobradores.requestFocus();
            resp = true;
        }

        if (lm1.getSize() == 0) {
            cmbRutas.requestFocus();
            resp = true;
        }
        return resp;
    }

    public void removeRoute() {
        String idRuta = listRutas.getSelectedValue();
        if (Rutas.contains(idRuta)) {
            Rutas.remove(idRuta);
        }
        updateShowText();
    }

    public void addRoute() {
        String idRuta = cmbRutas.getSelectedItem().toString();
        if (!Rutas.contains(idRuta) && !"SIN RUTAS".equals(idRuta)) {
            Rutas.add(idRuta);
        }
        updateShowText();
    }

