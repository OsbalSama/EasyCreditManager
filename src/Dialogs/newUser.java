    public boolean checkFields() {
        boolean resp = false;
        if (txtUsername.getText().length() > 0) {
            if (txtPassword.getText().length() > 0) {
                if (txtConfirmPassword.getText().length() > 0) {
                    if (allUsers.findUserByName(txtUsername.getText().replace(',', ' ')) == null) {
                        if (txtPassword.getText().equals(txtConfirmPassword.getText())) {
                            resp = true;
                        } else {
                            txtConfirmPassword.setText("");
                            txtConfirmPassword.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "<html><h1>ESTE USUARIO YA EXISTE!</h1><font SIZE=5><p>El usuario " + txtUsername.getText().replace(',', ' ') + " ya esta registrado... vuelva a intentarlo</font></html>", "¬°Atenci√≥n!", JOptionPane.WARNING_MESSAGE);
                        txtUsername.requestFocus();
                    }
                } else {
                    txtConfirmPassword.requestFocus();
                }
            } else {
                txtPassword.requestFocus();
//                JOptionPane.showMessageDialog(null, "<html><h1>¬°Cuidado faltan datos!</h1><font SIZE=5><p>Falta escribir la contrase√±a, verifique y vuelva a intentarlo</font></html>", "¬°Atenci√≥n!", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            txtUsername.requestFocus();
//            JOptionPane.showMessageDialog(null, "<html><h1>¬°Cuidado faltan datos!</h1><font SIZE=5><p>Falta escribir el nombre del usuario, verifique y vuelva a intentarlo</font></html>", "¬°Atenci√≥n!", JOptionPane.WARNING_MESSAGE);
        }
        return resp;
    }

    public void storeUser() {
        if (checkFields()) {
            int dialogButton = JOptionPane.showConfirmDialog(null, "<html><h1>¬øCREAR CUENTA?  </h1><font SIZE=5><p> Verifique bien sus datos antes de continuar‚Ä¶</font></html>", "CREAR NUEVA CUENTA", JOptionPane.OK_CANCEL_OPTION);
            if (dialogButton == JOptionPane.OK_OPTION) {                
                User.setUsername(new Encryptor().clearText(txtUsername.getText().replace(',', ' ')));
                User.setPassword(txtPassword.getText().replace(',', ' '));
                User.setRole(cmbRole.getSelectedItem().toString());
                if (allUsers.createUser(User)) {
                    ok = true;
                    JOptionPane.showMessageDialog(null, "<html><h1>CUENTA CREADA CORRECTAMENTE</h1><font SIZE=5><p> Clic para cerrar</font></html>", "Terminado", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
            }
        }
    }

