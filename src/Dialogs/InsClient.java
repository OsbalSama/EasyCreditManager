    public void cmbActionPreformed(String Item) {
        if (Item.equals("+ Nueva Ruta")) {
            insRuta();
        } else if (!Item.equals("SIN RUTAS")) {
            txtRuta.setText(new Rutas().getRutaToString(Item));
            txtRuta.setCaretPosition(0);
        }
    }

    public void insRuta() {
        txtRuta.setText("INSERTAR NUEVA RUTA");
        InsRuta ir = new InsRuta(null, true);
        ir.insertRoute();
        ir.show();
        loadRutas();
    }

    public void delClient() {
        int dialogButton = JOptionPane.showConfirmDialog(null, "<html><h1>ELIMINAR CLIENTE DEL SISTEMA</h1><font SIZE=5><p> Se borrara de forma permanente</font></html>", "ELIMINAR CLIENTE", JOptionPane.YES_NO_OPTION);
        if (dialogButton == 0) {
            if (new clientController().DelClient(lblIDClient.getText())) {
                JOptionPane.showMessageDialog(null, "<html><h1>CLIENTE ELIMINADO CORRECTAMENTE</h1><font SIZE=5><p> Clic para cerrar</font></html>", "CLIENTE ELIMINAdo", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        }
    }

    public void loadRutas() {        
        cmbRutas.removeAllItems();
        List RUTAS = new Rutas().loadRutas();
        if (RUTAS.isEmpty()) {
            cmbRutas.addItem("SIN RUTAS");
            cmbRutas.addItem("+ Nueva Ruta");
        } else {
            for (int i = 0; i < RUTAS.size(); i++) {
                cmbRutas.addItem(RUTAS.get(i).toString());
            }
            cmbRutas.addItem("+ Nueva Ruta");
        }
        setPicture();
        
    }

