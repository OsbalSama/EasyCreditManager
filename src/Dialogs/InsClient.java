    public void loadActualLebel(String idClient, int nivel) {
        int[] prog = new LevelAdmin().getClientPanelData(idClient, nivel);
        lblLevel.setText(String.valueOf(prog[0]));

        if (nivel < 5) {
            pbProgress.setMaximum(prog[2]);
            pbProgress.setValue(prog[1]);
        } else {
            pbProgress.setMaximum(1);
            pbProgress.setValue(1);
        }
        lblRestantLebel.setText(new LevelAdmin().getNextLevel(idClient, nivel));
    }

    public void setNewLebel() {
        int[] prog = new LevelAdmin().getNewClientPanelDataData();
        lblLevel.setText(String.valueOf(prog[0]));
        pbProgress.setMaximum(prog[2]);
        pbProgress.setValue(prog[1]);
        lblRestantLebel.setText(new LevelAdmin().getNewtLevel(1));
    }

