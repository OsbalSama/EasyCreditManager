    private void btn_sigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sigActionPerformed
        String[] serial = {txt_serial0.getText(), txt_serial1.getText(), txt_serial2.getText(), txt_serial3.getText(), txt_serial4.getText(), txt_serial5.getText()};
        if (new Licencias().validar_serial(serial) == true) {
            resp = 1;
            JOptionPane.showMessageDialog(null, "Clave de producto valida", "¡Bienvenido!", JOptionPane.INFORMATION_MESSAGE);
            String serl = serial[0] + "-" + serial[1] + "-" + serial[2] + "-" + serial[3] + "-" + serial[4] + "-" + serial[5];
            new ConfigDat().setLicData(txtCuenta.getText().replace(",", ""), txtemail.getText(), serl);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Clave de producto no valida", "¡Atencion!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_sigActionPerformed


