    public void act_sig() {
        if (!"".equals(txt_serial0.getText()) && !"".equals(txt_serial1.getText()) && !"".equals(txt_serial2.getText()) && !"".equals(txt_serial3.getText()) && !"".equals(txt_serial4.getText()) && !"".equals(txt_serial5.getText())) {
            if (txt_serial0.getText().length() == 4 && txt_serial1.getText().length() == 4 && txt_serial2.getText().length() == 4 && txt_serial3.getText().length() == 4 && txt_serial4.getText().length() == 4 && txt_serial5.getText().length() == 5) {
                if (!txtCuenta.getText().equals("") && !txtemail.getText().equals("")) {
                    btn_sig.setEnabled(true);
                } else {
                    btn_sig.setEnabled(false);
                }
            } else {
                btn_sig.setEnabled(false);
            }
        } else {
            btn_sig.setEnabled(false);
        }
    }

