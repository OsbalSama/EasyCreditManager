    private void txtPagoConKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPagoConKeyReleased
        if (!txtPagoCon.getText().replace(" ", "").equals("")) {
            if (txtPagoCon.getText().equals(".")) {
                txtPagoCon.setText("0.");
            }
            if (!txtAbonar.getText().replace(" ", "").equals("") && Double.parseDouble(txtPagoCon.getText()) < Double.parseDouble(txtAbonar.getText())) {
                txtPagoCon.setBackground(Color.RED);
                txtPagoCon.setForeground(Color.WHITE);
            } else {
                txtPagoCon.setBackground(Color.WHITE);
                txtPagoCon.setForeground(Color.BLACK);
            }
            recalc_total();
        } else {
            lbl_cambio.setText(new DecimalFormat("0.00").format(0));
            recalc_total();
        }
    }//GEN-LAST:event_txtPagoConKeyReleased

