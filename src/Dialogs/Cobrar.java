    public boolean compCacillas() {
        boolean resp = false;
        if (txtAbonar.getText().equals("")) {
            txtAbonar.requestFocus();
            resp = true;
        } else if (txtPagoCon.getText().equals("")) {
            txtPagoCon.requestFocus();
            resp = true;
        } else if (txtAbonar.getText().equals("") || Double.parseDouble(txtAbonar.getText()) > Double.parseDouble(txtPendient.getText())) {
            txtAbonar.requestFocus();
            resp = true;
        } else if (Double.parseDouble(txtPagoCon.getText()) < Double.parseDouble(txtAbonar.getText())) {
            txtPagoCon.requestFocus();
            resp = true;
        }
        return resp;
    }

    public void cobrar() {
        try {
            if (!compCacillas()) {
                int resp = JOptionPane.showConfirmDialog(null, "Los datos en la compra se guardarán…\n¿seguro que deseas continuar?", "Cancelar", JOptionPane.YES_NO_OPTION);
                if (resp == 0) {
                    asign_datos();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e, "¡ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void asign_datos() {
        String nreg = new CreditHistory().genID() + "," + txtProspect.getText() + "," + txtAbonar.getText() + "," + new SimpleDateFormat("dd-MM-YYYY").format(new Date()) + "," + new SimpleDateFormat("HH:MM").format(new Date()) + ",";
        new CreditHistory().InsCredHistRegistry(nreg);
        PrintDoc();
        dispose();
    }

