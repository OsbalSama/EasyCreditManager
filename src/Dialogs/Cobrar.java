    public void recalc_total() {
        try {
            if (!txtPagoCon.getText().equals("")) {
                double total = Double.parseDouble(txtPagoCon.getText()) - Double.parseDouble(txtAbonar.getText());
                lbl_cambio.setText(String.valueOf(new DecimalFormat("0.00").format(total)));
            } else {
                lbl_cambio.setText(new DecimalFormat("0.00").format(0));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e);
        }
    }

